#include <stdio.h>

void main(void) {
	int arr[2][4] = {
		{10,20,30,40},
		{50,60,70,80}
	};
	for (int i = 0; i < 2; i++)
	{
		for (int j = 0; j < 2; j++)
			printf("%d", arr[i][j]);
		printf("\n");
	}
	//배열은 연속된 메모리공간에 할당받기 때문에 1차원배열처럼 초기화도 가능
	int arr2[2][4] = { 10,20,30,40,50,60,70,80 };
	*(*(arr2 + 1) + 3) = 800;
	*(arr2[1] + 2) = 700;
	arr2[1][1] = 600;
}